""" Variables """
# Defining a variable
name = "Hashan"

# this will print the value of the variable
print(name)

# again change the variable
name = "Bhashini"

# printing the value of the variable again 
print(name)

# leaving an empty line
print()

# using variables with strings
print("My name is " + name)
print("My name is", name)
print("My name is {}".format(name))
print(f"My name is {name}")